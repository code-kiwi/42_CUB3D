#include "cub3d.h"
#include "libft.h"

/**
 * @brief Initializes the given t_game
 * @param game The structure to init
 * @return true on SUCCESS, false on ERROR
*/
bool	t_game_init(t_game *game)
{
	if (game == NULL)
		return (false);
	ft_memset(game, 0, sizeof(t_game));
	return (true);
}
