#include "cub3d.h"

void	double_array_copy(char dest[13][20], char src[13][20], size_t size_x, size_t size_y)
{
	for (size_t i = 0; i < size_y; i ++)
	{
		for (size_t j = 0; j < size_x; j ++)
		{
			dest[i][j] = src[i][j];
		}
	}
}

